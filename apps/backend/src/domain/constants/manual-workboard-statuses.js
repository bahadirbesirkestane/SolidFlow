const MANUAL_WORKBOARD_STATUSES = {
  WAITING: "Beklemede",
  PREPARING: "Hazirlaniyor",
  IN_PROGRESS: "Devam Ediyor",
  REVIEW: "Kontrol Ediliyor",
  COMPLETED: "Tamamlandi",
};

const MANUAL_WORKBOARD_PROGRESS_MAP = {
  [MANUAL_WORKBOARD_STATUSES.WAITING]: 0,
  [MANUAL_WORKBOARD_STATUSES.PREPARING]: 25,
  [MANUAL_WORKBOARD_STATUSES.IN_PROGRESS]: 50,
  [MANUAL_WORKBOARD_STATUSES.REVIEW]: 75,
  [MANUAL_WORKBOARD_STATUSES.COMPLETED]: 100,
};

const ALL_MANUAL_WORKBOARD_STATUSES = Object.values(MANUAL_WORKBOARD_STATUSES);
const MANUAL_WORKBOARD_MAX_DEPTH = 3;

function resolveManualWorkboardProgress(status) {
  return MANUAL_WORKBOARD_PROGRESS_MAP[String(status || "").trim()] ?? 0;
}

module.exports = {
  MANUAL_WORKBOARD_STATUSES,
  MANUAL_WORKBOARD_PROGRESS_MAP,
  ALL_MANUAL_WORKBOARD_STATUSES,
  MANUAL_WORKBOARD_MAX_DEPTH,
  resolveManualWorkboardProgress,
};
